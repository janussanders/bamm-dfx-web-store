/** Map regulated or stale backend descriptions to compliant storefront copy. */
export function featureMarketingDescription(
  licenseReferenceName: string,
  fallback: string,
): string {
  switch (licenseReferenceName) {
    case "Trades":
      return TRADES_FEATURE_BLURB;
    case "Tx Simulator":
      return TX_SIMULATOR_FEATURE_BLURB;
    default:
      return fallback;
  }
}

export function isRegulatedFeature(licenseReferenceName: string): boolean {
  return (
    licenseReferenceName === "Trades" || licenseReferenceName === "Tx Simulator"
  );
}

export function regulatedFeatureCardDisclaimer(
  licenseReferenceName: string,
): string | null {
  if (licenseReferenceName === "Trades") return TRADES_CARD_DISCLAIMER;
  if (licenseReferenceName === "Tx Simulator") return TX_CARD_DISCLAIMER;
  return null;
}
