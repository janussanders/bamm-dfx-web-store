#!/usr/bin/env node
/**
 * Stamp storefront version for UI (DDR-029).
 * Writes src/frontend/src/generated/storefrontVersion.ts from git tag or CLI arg.
 *
 * Usage:
 *   node scripts/write-storefront-version.mjs           # git describe
 *   node scripts/write-storefront-version.mjs v133.0.13 # explicit tag
 */

import { execSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const OUT = path.join(ROOT, 'src/frontend/src/generated/storefrontVersion.ts');

function fail(message) {
  console.error(`\nwrite-storefront-version FAILED: ${message}`);
  process.exit(1);
}

function resolveTag() {
  const arg = process.argv[2];
  if (arg) {
    if (!/^v?\d+\.\d+\.\d+/.test(arg) && arg !== 'dev') {
      // allow git describe style too
    }
    return arg.startsWith('v') || arg === 'dev' ? arg : `v${arg}`;
  }
  try {
    const described = execSync('git describe --tags --exact-match HEAD', {
      cwd: ROOT,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();
    if (described) return described;
  } catch {
    // not on exact tag
  }
  try {
    return execSync('git describe --tags --always --dirty', {
      cwd: ROOT,
      encoding: 'utf8',
    }).trim();
  } catch {
    return 'dev';
  }
}

const tag = resolveTag();
const commit = (() => {
  try {
    return execSync('git rev-parse --short HEAD', {
      cwd: ROOT,
      encoding: 'utf8',
    }).trim();
  } catch {
    return 'unknown';
  }
})();

const contents = `/* Generated by scripts/write-storefront-version.mjs — do not edit by hand */
export const STOREFRONT_VERSION = ${JSON.stringify(tag)} as const;
export const STOREFRONT_COMMIT = ${JSON.stringify(commit)} as const;
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, contents, 'utf8');
console.log(`storefront version stamped: ${tag} (${commit}) → ${path.relative(ROOT, OUT)}`);
